async function generateWithClaude(title: string, overview: string | null): Promise<string | null> {
  try {
    const { default: Anthropic } = await import("@anthropic-ai/sdk");
    const client = new Anthropic();
    const msg = await client.messages.create({
      model: "claude-opus-4-8", // 大量生成でコスト優先なら claude-haiku-4-5 に変更可
      max_tokens: 120,
      system: DOMIRU_SYSTEM,
      messages: [{ role: "user", content: `作品名: ${title}\n参考あらすじ: ${overview ?? "（なし）"}\n\nこの作品の感想を一言で。` }],
    });
    const text = msg.content.find((b) => b.type === "text");
    return text && "text" in text ? text.text.trim() : null;
  } catch (e) {
    console.warn(`Claude生成失敗(${title}):`, (e as Error).message);
    return null;
  }
}

async function main() {
  const useClaude = !!process.env.ANTHROPIC_API_KEY;
  console.log(`どみるAI感想: ${useClaude ? "Claude生成" : "手書きフォールバック"}`);
  const works = await prisma.work.findMany();
  let filled = 0;
  for (const w of works) {
    const comment = useClaude ? await generateWithClaude(w.title, w.overview) : COMMENTS[w.title] ?? null;
    if (comment) {
      await prisma.work.update({ where: { id: w.id }, data: { aiComment: comment } });
      filled++;
    }
  }
  console.log(`感想を ${filled}/${works.length} 作品に投入。`);
}

main().finally(() => prisma.$disconnect());
